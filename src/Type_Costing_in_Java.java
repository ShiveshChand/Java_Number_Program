public class Type_Costing_in_Java {
    public static void main(String[] args) {
        byte byte_Value = 10;
        short short_value = 20;
        int int_value = 50;
        long long_total = 50000 + 10 *(byte_Value+short_value+int_value);
        System.out.println(long_total);
        short shortTotal = (short) (1000+10*(byte_Value+short_value+int_value));
        System.out.println(shortTotal);






    }
}
