public class Data_Type {
    public static void main(String[] args) {
        int max = Integer.MAX_VALUE;
        System.out.println("Integer.MAX_VALUE = "+max);
        int min = Integer.MIN_VALUE;
        System.out.println(" = "+min);
        int max1 = Integer.MAX_VALUE+1;
        System.out.println("integer max+1 value = "+max1); // Int Max value = 2147483647 = 2147483647+1= Overflow
        int min1 = Integer.MIN_VALUE-1;
        System.out.println("integer min+1 value = "+min1);
        byte B = Byte.MAX_VALUE;
        byte b = Byte.MIN_VALUE;
        System.out.println("Byte.MAX_VALUE = "+B);
        System.out.println("Byte.MIN_VALUE = "+b);
        short S = Short.MAX_VALUE;
        short s = Short.MIN_VALUE;
        System.out.println("Short.MAX_VALUE = "+S);
        System.out.println("Short.MIN_VALUE = "+s);
        long L = Long.MAX_VALUE;
        System.out.println("Long.MAX_VALUE = "+L);
        long l = Long.MIN_VALUE;
        System.out.println("Long.MIN_VALUE = "+l);
        float F = Float.MAX_VALUE;
        System.out.println("Float.MAX_VALUE = "+F);
        float f = Float.MIN_VALUE;
        System.out.println("Float.MIN_VALUE"+f);



    }
}
